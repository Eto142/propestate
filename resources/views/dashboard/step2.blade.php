                merlinLabel.getAttribute(
                  "data-merlin-select-clicked"
                ) == "1"
              ) {
                if (
                  e.keyCode == 38
                ) {
                  // Handle ArrowUp.
                  e.preventDefault();
                  arrowNav(1);
                }
                if (
                  e.keyCode == 40
                ) {
                  // Handle ArrowDown.
                  e.preventDefault();
                  arrowNav(0);
