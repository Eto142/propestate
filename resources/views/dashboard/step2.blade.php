                      ].dispatchEvent(
                        event
                      );
                    }
                  }
                }
                if (
                  e.keyCode == 9
                ) {
                  // Handle tab.
                  var event =
                    new Event(
                      "blur",
                      {
                        view: window,
                        bubbles: true,
                        cancelable: true,
                      }
                    );
                  merlinLabel.dispatchEvent(
                    event
                  );
                }
              }
            };

